// Arm Automation
void left()
{
  // BASE
    servo2.write(0); // j1 - 26
    servo3.write(0); // j2 - 32
    servo4.write(0); // wrist - 33
    servo5.write(40); // grabber - 33
    delay(1000);
    servo1.write(0); // base - 25

    delay(3000);

  //move above box
    servo1.write(90);
    delay(2000);
  
  //open arm
    servo5.write(45); // grabber - 33
    delay(1000);

    // arm down to lift
    for (pos = 0; pos <= 55; pos += 1)
    { // move motor slowly
        servo2.write(pos);
        servo3.write(0);
        delay(50);
    }

    servo5.write(grabForce); // grabbed dustbin
    delay(2000);

    // arm move up

    for (pos = 55; pos >= 20; pos -= 1)
    { // move motor slowly
        servo2.write(pos);
        servo3.write(0);
        delay(50);
    }

    delay(2000);

    // base move over tank
    servo1.write(180);
    delay(2000);

    servo4.write(150); // unload kachra
    delay(2000);
    servo4.write(0); // normal dustbin orientation
    delay(2000);

    servo1.write(90);
    delay(2000);

    //arm down
    for (pos = 20; pos <= 55; pos += 1)
    { // move motor slowly
        servo2.write(pos);
        servo3.write(0);
        delay(50);
    }
    /*   servo2.write(65);
      delay(2000);
      servo3.write(30); // MOTOR NOT WORKING
       */
    delay(2000);

    servo5.write(40); // removed dustbin
    delay(1000);
    //move servo up
       for (pos = 55; pos <= 0; pos += 1)
    { // move motor slowly
        servo2.write(pos);
        servo3.write(0);
        delay(50);
    }

    delay(5000);
    // return all except base
    servo2.write(0); // j1 - 26
    servo3.write(0); // j2 - 32
    servo4.write(0); // wrist - 33
    servo5.write(40); // grabber - 33
    delay(5000);
}

