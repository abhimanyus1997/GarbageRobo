// u;ltrasonic_sensor
float sonar()
{
  // Clears the trigPin
  digitalWrite(trigPin, LOW);
  delayMicroseconds(2);
  // Sets the trigPin on HIGH state for 10 micro seconds
  digitalWrite(trigPin, HIGH);
  delayMicroseconds(10);
  digitalWrite(trigPin, LOW);

  // Reads the echoPin, returns the sound wave travel time in microseconds
  duration = pulseIn(echoPin, HIGH);

  // Calculate the distance
  distanceCm = duration * SOUND_SPEED / 2;

  // Convert to inches
  distanceInch = distanceCm * CM_TO_INCH;

  // Prints the distance in the Serial Monitor
  /*   Serial.print("Distance (cm): ");
    Serial.println(distanceCm);
    Serial.print("Distance (inch): ");
    Serial.println(distanceInch); */
  return distanceCm;
}

void setup()
{
  Serial.begin(115200);
  SerialBT.begin();
  // oled_start
  oled.init();
  oled.clear();
  oled.update();
  oled.home();
  oled.drawBitmap(0, 0, garblogo, 128, 64); // по умолч. нормал и BUF_ADD
  oled.update();

  // Serial.println("Bluetooth Started! Ready to pair...");
  // motors
  pinMode(in1, OUTPUT);
  pinMode(in2, OUTPUT);
  pinMode(in3, OUTPUT);
  pinMode(in4, OUTPUT);
  pinMode(laser, OUTPUT);
  // Turn off motors - Initial state
  digitalWrite(in1, LOW);
  digitalWrite(in2, LOW);
  digitalWrite(in3, LOW);
  digitalWrite(in4, LOW);

  // ULTRASONIC
  pinMode(trigPin, OUTPUT); // Sets the trigPin as an Output
  pinMode(echoPin, INPUT);  // Sets the echoPin as an Input

  // SERVO
  //  Allow allocation of all timers
  ESP32PWM::allocateTimer(0);
  ESP32PWM::allocateTimer(1);
  ESP32PWM::allocateTimer(2);
  ESP32PWM::allocateTimer(3);
  servo1.setPeriodHertz(50); // Standard 50hz servo
  servo2.setPeriodHertz(50); // Standard 50hz servo
  servo3.setPeriodHertz(50); // Standard 50hz servo
  servo4.setPeriodHertz(50); // Standard 50hz servo
  servo4.setPeriodHertz(50); // servo5.setPeriodHertz(50);      // Standard 50hz servo

  delay(1000);
}

void loop()
{
  if (SerialBT.available())
  {
    BTData = SerialBT.read();
    Serial.write(BTData);
  }
  char Data = (char)BTData;

  if (Data == 'w')
  {
    // Now turn FWD motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, HIGH);
    digitalWrite(in3, LOW);
    digitalWrite(in4, HIGH);
  }

  else if (Data == 's')
  {
    // Now turn FWD motors
    digitalWrite(in1, HIGH);
    digitalWrite(in2, LOW);
    digitalWrite(in3, HIGH);
    digitalWrite(in4, LOW);
  }

  else if (Data == 'd')
  {
    // Now right motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, LOW);
    digitalWrite(in3, LOW);
    digitalWrite(in4, HIGH);
  }
  else if (Data == 'D')
  {
    // Now xtreme right motors
    digitalWrite(in1, HIGH);
    digitalWrite(in2, LOW);
    digitalWrite(in3, LOW);
    digitalWrite(in4, HIGH);
  }

  else if (Data == 'a')
  {
    // Now left motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, HIGH);
    digitalWrite(in3, LOW);
    digitalWrite(in4, LOW);
  }
  else if (Data == 'A')
  {
    // Now xtreme left motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, HIGH);
    digitalWrite(in3, HIGH);
    digitalWrite(in4, LOW);
  }

  else if (Data == '1')
  {
    // Now stop motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, LOW);
    digitalWrite(in3, LOW);
    digitalWrite(in4, LOW);
    arm_initiate();
  }
  else
  {
    // Now stop motors
    digitalWrite(in1, LOW);
    digitalWrite(in2, LOW);
    digitalWrite(in3, LOW);
    digitalWrite(in4, LOW);
  }
  oled.clear();
  oled.home();
  oled.rect(0, 0, 120, 56, OLED_STROKE);
  oled.autoPrintln(true);
  // oled.setscale(1);
  oled.setCursor(5, 1);
  oled.print("Picker GUI");
  oled.setCursor(5, 5);
  float obsctacle = sonar();
  oled.print("Distance: " + (String)obsctacle + "cm");
  if (obsctacle <= 50 && obsctacle > 15)
  {
    digitalWrite(laser, HIGH);
  }
/*   else if (obsctacle <= 15 && obsctacle > 0)
  {
    while (i < 10)
    {
      digitalWrite(laser, LOW);
      delay(500);
      digitalWrite(laser, HIGH);
      i++;
    }
  } */
  else
    {
    digitalWrite(laser, LOW);
  }

  oled.update();
  delay(100);
}
